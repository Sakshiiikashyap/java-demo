import java.util.*;
public class even{
    public static void main(String[] args){
        Scanner sc = new Scanner(system.in);
        System.out.print("Enter the number =");
        int a = sc.nextInt();
        if(a%2==0){
            System.out.println("The number is even");
        } else{
            System.out.println("The number is odd");
        }
    }
}